.MODEL SMALL
.STACK 100H
.DATA
Number DB 33H

POSITIVE DB 50H
NEGATIVE DB 4EH
ZERO DB 5AH

.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX

    MOV AL, Number

    CMP AL,0
    JE ZER
    JL NEG

POS:
    MOV DL, POSITIVE
    MOV AH,2
    INT 21H
    JMP EXIT

NEG:
    MOV DL, NEGATIVE
    MOV AH,2
    INT 21H
    JMP EXIT

ZER:
    MOV DL, ZERO
    MOV AH,2
    INT 21H

EXIT:
    MOV AH,4CH
    INT 21H

MAIN ENDP
END MAIN
