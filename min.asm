.MODEL SMALL
.STACK 100H
.DATA
A DB 3,1,4,2,5
LEN EQU 5
MIN DB ?

.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX

    MOV SI,0
    MOV AL,A[SI]
    MOV MIN,AL

    INC SI
    MOV CX,LEN
    DEC CX

FINDMIN:
    MOV AL,A[SI]
    MOV BL,MIN
    CMP AL,BL
    JGE CONTINUE_MIN
    MOV MIN,AL

CONTINUE_MIN:
    INC SI
    LOOP FINDMIN

    MOV AL,MIN
    ADD AL,30H
    MOV DL,AL
    MOV AH,2
    INT 21H

    MOV AH,4CH
    INT 21H
MAIN ENDP
END MAIN
