.MODEL SMALL
.STACK 100H
.DATA
A DB 5,4,3,2,1
B EQU 5

.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX

    MOV SI,0
    MOV CX,B

DECREMENT:
    DEC A[SI]
    INC SI
    LOOP DECREMENT

    MOV SI,0
    MOV CX,B

PRINT:
    MOV DL,A[SI]
    ADD DL,30H
    MOV AH,2
    INT 21H
    INC SI
    LOOP PRINT

    MOV AH,4CH
    INT 21H
MAIN ENDP
END MAIN
