.MODEL SMALL
.STACK 100H
.DATA
A DB 3,1,4,2,5
LEN EQU 5
MAX DB ?

.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX

    MOV SI,0
    MOV AL,A[SI]
    MOV MAX,AL

    INC SI
    MOV CX,LEN
    DEC CX

FINDMAX:
    MOV AL,A[SI]
    MOV BL,MAX
    CMP AL,BL
    JLE CONTINUE
    MOV MAX,AL

CONTINUE:
    INC SI
    LOOP FINDMAX

    MOV AL,MAX
    ADD AL,30H
    MOV DL,AL
    MOV AH,2
    INT 21H

    MOV AH,4CH
    INT 21H
MAIN ENDP
END MAIN
